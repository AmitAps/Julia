function goldisays(porrdegreesC)
    if porrdegreesC > 45
        println("Goldilocks says: 'This porridge is too hot!'")
    elseif porrdegreesC < 45
        println("Goldilocks says: 'This porridge is too cold!'")
    else
        println("Goldilocks says: 'This porridge is just right!'")
    end
end